import mongoose from "mongoose";

const SecurityLogSchema = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
    text: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const SecurityLog = mongoose.model("SecurityLog", SecurityLogSchema);

export default SecurityLog;
